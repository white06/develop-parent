package com.tdu.develop.user.pojo;


public class Subjects{

	private String id;
	private String subjectName;
	private String subjectIcon;
	private String money;
	private String subjectIntro;
	private String subjectOrder;
	private String major_Id;
	private String data;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSubjectName() {
		return subjectName;
	}
	public void setSubjectName(String subjectName) {
		this.subjectName = subjectName;
	}
	public String getSubjectIcon() {
		return subjectIcon;
	}
	public void setSubjectIcon(String subjectIcon) {
		this.subjectIcon = subjectIcon;
	}
	public String getMoney() {
		return money;
	}
	public void setMoney(String money) {
		this.money = money;
	}
	public String getSubjectIntro() {
		return subjectIntro;
	}
	public void setSubjectIntro(String subjectIntro) {
		this.subjectIntro = subjectIntro;
	}
	public String getSubjectOrder() {
		return subjectOrder;
	}
	public void setSubjectOrder(String subjectOrder) {
		this.subjectOrder = subjectOrder;
	}
	public String getMajor_Id() {
		return major_Id;
	}
	public void setMajor_Id(String major_Id) {
		this.major_Id = major_Id;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
}
