package com.tdu.develop.resource.pojo;

public class TestList {
	private String Id;
	private String ExamType;
	private String Name;
	private String ClassName;
	private String PagerName;
	private String CorrectPrenson;
	private String StartTime;
	private String EndTime;
	
	public String getId() {
		return Id;
	}
	public void setId(String id) {
		Id = id;
	}
	public String getExamType() {
		return ExamType;
	}
	public void setExamType(String examType) {
		ExamType = examType;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getClassName() {
		return ClassName;
	}
	public void setClassName(String className) {
		ClassName = className;
	}
	public String getPagerName() {
		return PagerName;
	}
	public void setPagerName(String pagerName) {
		PagerName = pagerName;
	}
	public String getCorrectPrenson() {
		return CorrectPrenson;
	}
	public void setCorrectPrenson(String correctPrenson) {
		CorrectPrenson = correctPrenson;
	}
	public String getStartTime() {
		return StartTime;
	}
	public void setStartTime(String startTime) {
		StartTime = startTime;
	}
	public String getEndTime() {
		return EndTime;
	}
	public void setEndTime(String endTime) {
		EndTime = endTime;
	}
	
}
